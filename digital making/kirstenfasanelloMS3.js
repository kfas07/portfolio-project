function setup() {
  createCanvas(500, 500);
  frameRate(25);
  rectMode(CENTER);

  xVal = 450;
  yVal = 70;

  // these variables are assigned ASCII decimal values
  A = 65;
  D = 68;
  S = 83;
  W = 87;
}

function draw() {
  background(220);

  textSize(25);
  fill(31,29,88);
  text(key, 450, 150);
  drawMaze();

  // if keys A, D, S, or W are pressed the purple square moves respectively 
  if (keyCode === A) {
    xVal -= 5;
  } else if (keyCode === D) {
    xVal += 5;
  } else if (keyCode === S) {
    yVal += 5;
  } else if (keyCode === W) {
    yVal -= 5;
  }
  
  // if statement to determine if player won
  if(yVal - 27  > 330 && xVal - 27 < 75) {
    textSize(80)
    background(0, 0, 130, 80)
    text('YOU WON!!', 20, height / 2);
  }
  
  textSize(20);
  fill(31,29,88);
  text('Start', 420, 40);
  text('End', 3, 425);
  fill(220, 205, 247);
  rect(xVal, yVal, 20, 20);
}

